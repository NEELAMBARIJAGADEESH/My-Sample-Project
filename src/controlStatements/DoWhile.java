package controlStatements;

public class DoWhile {
	public static void main(String[] args) {
		int a=1;
		//****while loop****
		while (a<=5) {
			System.out.println(a);
			a++;
			
		}
		System.out.println();
		//****do while
		int b=1;
		do {
			System.out.println(b);
			b++;
		}while (b>=5);
		
	}
}
